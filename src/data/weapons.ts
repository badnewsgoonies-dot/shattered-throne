import {
  Element,
  ItemCategory,
  MovementType,
  WeaponData,
  WeaponType,
} from '../shared/types';

interface WeaponSpec {
  id: string;
  name: string;
  description: string;
  weaponType: WeaponType;
  might: number;
  hit: number;
  crit: number;
  minRange: number;
  maxRange: number;
  weight: number;
  maxDurability: number;
  cost: number;
  rank: string;
  element?: Element;
  effectiveAgainst?: MovementType[];
  specialEffect?: string;
}

function weapon(spec: WeaponSpec): WeaponData {
  return {
    id: spec.id,
    name: spec.name,
    description: spec.description,
    category: ItemCategory.Weapon,
    weaponType: spec.weaponType,
    might: spec.might,
    hit: spec.hit,
    crit: spec.crit,
    range: { min: spec.minRange, max: spec.maxRange },
    weight: spec.weight,
    maxDurability: spec.maxDurability,
    element: spec.element,
    effectiveAgainst: spec.effectiveAgainst,
    specialEffect: spec.specialEffect,
    cost: spec.cost,
    rank: spec.rank,
  };
}

